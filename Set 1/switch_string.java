class switch_string
{
    public static void main(String abc)
    {
        switch(abc)
        {
            case "Sunday":System.out.println("Daynum : 1");
            break;
            case "Monday":System.out.println("Daynum : 2");
            break;
            case "Tuesday":System.out.println("Daynum : 3");
            break;
            case "Wednesday":System.out.println("Daynum : 4");
            break;
            case "Thursday":System.out.println("Daynum : 5");
            break;
            case "Friday":System.out.println("Daynum : 6");
            break;
            case "Saturday":System.out.println("Daynum : 7");
            break;
            default:System.out.println("Enter a day Name");
        }
    }
}